require('../../config.php');

// Include WB admin wrapper script
$update_when_modified = true; // Tells script to update when this page was last updated
require(WB_PATH.'/modules/admin.php');

// This code removes any php tags and adds slashes
$header = $admin->add_slashes(str_replace('?php', '', $_POST['header']));
$footer = $admin->add_slashes(str_replace('?php', '', $_POST['footer']));
$template_details = $admin->add_slashes(str_replace('?php', '', $_POST['template_details']));

// Update settings
$database->query("UPDATE ".TABLE_PREFIX."mod_rbtoggle_settings SET "
	." header = '$header', "
	." footer = '$footer', "
	." template_details = '$template_details' "
	." WHERE section_id = '$section_id'");

// Check if there is a db error, otherwise say successful
if($database->is_error()) {
	$admin->print_error($database->get_error(), ADMIN_URL.'/pages/modify.php?page_id='.$page_id);
} else {
	$admin->print_success($TEXT['SUCCESS'], ADMIN_URL.'/pages/modify.php?page_id='.$page_id);
}

// Print admin footer
$admin->print_footer();

